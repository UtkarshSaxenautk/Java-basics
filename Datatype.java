public class Datatype {
    public static void main(String[] args)
    {
        int x = 7 ;
        double d = 5.987;
        float f = (float) 8.07 ;
        byte y = 6 ;
        String s = "100";
        short a = 5;
        long l = 678578;
        int r = Integer.parseInt(s);
        System.out.println("This is int : " + x);
        System.out.println("This is double : " + d);
        System.out.println("This is float : " + f);
        System.out.println("This is byte : " + y);
        System.out.println("This is String : " + s);
        System.out.println("This is short : " + a);
        System.out.println("This is long : " + l);
        System.out.println("This is int from string : " + r);

    }
}
