package BitwiseOperator;

public class Operators {
    public static void main(String[] args) {
        // XOR OPERATOR 
        int x = 3 ;
        int y = 2 ;
        System.out.println("Xor of " + x + " and " + y + " = " + (x ^ y));
        // OR operator 
        System.out.println("OR of " + x + " and " + y + " = " + (x | y));
        // And operator
        System.out.println("And of " + x + " and " + y + " = " + (x & y));

    }
}

